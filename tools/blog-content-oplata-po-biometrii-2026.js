<p>Это самый серьёзный и обоснованный риск. В отличие от пароля, биометрию нельзя сменить. Если утечёт ваша база паролей — вы меняете пароли. Если утечёт биометрическая база — вы не можете «поменять лицо». Инциденты с утечками биометрических данных уже случались в мире (например, утечка UIDAI в Индии или взлом Clearview AI). В России пока не было громких подтверждённых утечек из крупных биометрических баз — но это не гарантия.</p>

