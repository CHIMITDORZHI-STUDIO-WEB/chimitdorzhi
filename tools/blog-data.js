<p>Если ваш сайт ориентирован на россиян (русскоязычная версия, рубли в цене, доставка в РФ), вы тоже обязаны соблюдать 152-ФЗ. Локализация базы данных в РФ — обязательна. Реальный пример: Booking.com попал под блокировку из-за невыполнения этого требования.</p>

