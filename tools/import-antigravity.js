// ---------- утилиты ----------
function esc(s){ return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;'); }
function deEmoji(s){ return String(s==null?'':s).replace(EMOJI_RE,'').replace(/️/gu,''); }

// запрещённые соцсети → разрешённые/нейтральные
function fixSocials(s){
  s = s.replace(/WhatsApp/gi, 'Telegram');
  s = s.replace(/Instagram\*?/gi, 'ВКонтакте');
  s = s.replace(/инстаграм[а-я]*/gi, 'ВКонтакте');
  s = s.replace(/Facebook\*?/gi, 'ВКонтакте');
  s = s.replace(/фейсбук[а-я]*/gi, 'ВКонтакте');
  s = s.replace(/\b(TikTok|Тикток)\b/gi, 'VK Клипы');
  s = s.replace(/\bReels\b/gi, 'короткие видео');
  // дедуп после замен
  s = s.replace(/ВКонтакте(\s*[,и]\s*ВКонтакте)+/g, 'ВКонтакте');
  return s;
}

function inline(text){
  let s = esc(text);
  s = s.replace(/`([^`]+)`/g, (m,c)=>`<code>${c}</code>`);
  s = s.replace(/\*\*([^*]+)\*\*/g, '<strong>$1</strong>');
  s = s.replace(/\[([^\]]+)\]\(([^)\s]+)\)/g, (m,t,u)=>`<a href="${u}">${t}</a>`);
  return s;
}

function slugify(t){
  return t.toLowerCase().replace(/[^a-z0-9а-я\s-]/gi,'').trim().replace(/\s+/g,'-').slice(0,40) || 'sec';
}

function parseFrontmatter(src){
  const m = src.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/);
  const fm = {}; let body = src;
  if(m){
    body = src.slice(m[0].length);
    for(const line of m[1].split(/\r?\n/)){
      const mm = line.match(/^([a-zA-Z]+):\s*(.*)$/);
      if(!mm) continue;
      let [,k,v] = mm; v=v.trim();
      if(v.startsWith('[')){ // tags
        v = v.replace(/^\[|\]$/g,'').split(',').map(x=>x.trim().replace(/^['"]|['"]$/g,'')).filter(Boolean);
      } else { v = v.replace(/^['"]|['"]$/g,''); }
      fm[k]=v;
    }
  }
  return { fm, body };
}

