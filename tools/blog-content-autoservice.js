<p>SEO под Я.Карты и Я.Услуги — главный канал. Заполненная карточка, отзывы, фото, кнопка записи. Платное продвижение в выдаче на ключевые запросы — «замена масла [район]», «шиномонтаж [метро]», «диагностика [марка]».</p>
