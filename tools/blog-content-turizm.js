app.post("/webhooks/yandex-travel", async (req, res) => {
  // 1. Проверить подпись запроса
  const signature = req.headers["x-yandex-signature"] as string;
  const computed = crypto
    .createHmac("sha256", SECRET)
    .update(JSON.stringify(req.body))
    .digest("hex");

  if (signature !== computed) {
    return res.status(403).json({ error: "invalid signature" });
  }

  // 2. Обработать событие
  const event = req.body;
  switch (event.type) {
    case "booking.created":
      await createBookingInPMS({
        external_id: event.booking.id,
        guest_name: event.booking.guest.full_name,
        guest_phone: event.booking.guest.phone,
        room_type: event.booking.room.type,
        check_in: event.booking.dates.check_in,
        check_out: event.booking.dates.check_out,
        amount: event.booking.amount,
        commission: event.booking.commission,
        source: "yandex_travel",
      });
      break;

    case "booking.cancelled":
      await cancelBookingInPMS(event.booking.id);
      // Channel manager автоматически вернёт квоту на остальные OTA
      break;

    case "booking.modified":
      await updateBookingInPMS(event.booking);
      break;
  }

  res.json({ ok: true });
});

